num = 1000;
switch(num){
    case 1000:
        console.log('I am 1000');
        break;
    case 500:
        console.log('I am 500');
        break;
    case 100:
    case 200:
        console.log('I am either 100 & 200');
        break;
    default:
        console.log("I don't know who you are");
}